  $("#myform").submit(function(){

    $inputs = $('input:radio:checked');
    var s = 0;
    $inputs.each(function(index){
        s += 1.0*$($inputs[index]).val();
    });
    var t = "";
    var $p = $("#level");
    if(s>153){
      t = "Severe Autism";
    }else if(s>107){
      t = "Moderate Autism";
    }else if(s>70){
      t = "Mild Autism";
    }else{
      t = "No Autism";
    }
    $p.html(t);
    $('.modal').modal('show');
    return false;
  })

